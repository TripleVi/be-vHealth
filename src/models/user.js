class User {
    constructor(uid, username, email, name, dateOfBirth, gender, weight, height, avatarUrl) {
        this.uid = uid
        this.username = username
        this.email = email
        this.name = name
        this.dateOfBirth = dateOfBirth
        this.gender = gender
        this.weight = weight
        this.height = height
        this.avatarUrl = avatarUrl
    }
}

export default User